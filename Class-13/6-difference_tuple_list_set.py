# List: Ordered, mutable (can be modified), allows duplicate elements.
# Tuple: Ordered, immutable (cannot be modified), allows duplicate elements.
# Set: Unordered, mutable, does not allow duplicate elements.

# List
my_list = [1, 2, 3, 3]
print(my_list)  # [1, 2, 3, 3]

# Tuple
my_tuple = (1, 2, 3, 3)
print(my_tuple)  # (1, 2, 3, 3)

# Set
my_set = {1, 2, 3, 3}
print(my_set)  # {1, 2, 3}
