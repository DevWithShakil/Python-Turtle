# Using >= operator to check if a number is greater than or equal to 5
for number in range(1, 11):
    if number >= 5:
        print(number, "is greater than or equal to 5.")
    else:
        print(number, "is less than 5.")
        

# Using <= operator to check if a number is less than or equal to 3
for number in range(1, 11):
    if number <= 3:
        print(number, "is less than or equal to 3.")
    else:
        print(number, "is greater than 3.")

